// Solution: Using Compound Operators
// This demonstrates compound assignment operators (+=, -=)

void main() {
    // Start with score = 100
    int score = 100;
    
    // Add 50 using += operator
    score += 50;  // score is now 150
    
    // Subtract 30 using -= operator
    score -= 30;  // score is now 120
    
    // Print the final score
    IO.println(score);
}