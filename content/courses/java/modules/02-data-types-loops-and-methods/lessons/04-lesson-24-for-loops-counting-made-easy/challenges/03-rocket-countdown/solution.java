// Solution: Rocket Countdown
// This demonstrates counting backwards with a for loop

void main() {
    // For loop: start at 5, run while i > 0, decrement each time
    for (int i = 5; i > 0; i--) {
        IO.println(i);
    }
    
    // Print "Liftoff!" after the countdown
    IO.println("Liftoff!");
}